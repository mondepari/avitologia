#!/bin/bash
# Скрипт для быстрого запуска проекта в Docker

# Останавливаем скрипт при ошибках
set -e

echo "== Запуск проекта в Docker =="
echo "== $(date) =="

# Проверка наличия файла .env
if [ ! -f .env ]; then
  echo "Создание файла .env из примера..."
  cp .env.example .env
  echo "Файл .env создан. При необходимости отредактируйте его."
fi

# Запуск контейнеров
echo "Запуск Docker контейнеров..."
docker-compose up -d

# Проверка статуса
echo "Проверка статуса контейнеров..."
docker-compose ps

echo "== Запуск проекта завершен =="
echo "Приложение доступно по адресу: http://localhost:5000"
echo "Для просмотра логов выполните: docker-compose logs -f"